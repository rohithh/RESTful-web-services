    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        info("Grade Book Init submit pressed");
        boolean create = true;
        int optionSelected = 0;
        if(jTextField5.getText().equals("")){
            jTextField6.setText("TODO : Insert appropriate error code here"); // TODO
            jTextArea1.setText("Error : Please enter valid text into the text box");
            return;
        }
        if(jRadioButton6.isSelected()){
            optionSelected = 1;
            create = true;
        }
        else if(jRadioButton7.isSelected()){
            optionSelected = 2;
            create = true;
        }
        else if(jRadioButton1.isSelected()){
            optionSelected = 3;
            create = false;
        }
        else if(jRadioButton8.isSelected()){
            optionSelected = 4;
            create = false;
        }
        else {
            jTextField6.setText("TODO : Insert appropriate error code here"); // TODO
            jTextArea1.setText("Error : Please click one of the radio buttons to continue");
            return;
        }
        
        if(jTextField5.getText().equalsIgnoreCase("")){
            jTextField6.setText("TODO : Insert appropriate error code here"); // TODO
            jTextArea1.setText("Error : Please enter a correct value into the text box");                  
        }
        String fieldName = jTextField5.getText();
        if(create){
           Assignment2Client.clientPost(fieldName, optionSelected);            
        }
        else {
            Assignment2Client.clientColumnDelete(fieldName, optionSelected);
        }
            
            jTextField6.setText(Integer.toString(Assignment2Client.serverResponse.getStatus()));
            jTextArea1.setText(Assignment2Client.resourceRepresentation);
            jTextField4.setText(Assignment2Client.resourceUri);
    }//GEN-LAST:event_jButton4ActionPerformed

