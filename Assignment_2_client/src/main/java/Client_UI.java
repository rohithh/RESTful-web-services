    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        info("Submit pressed");
        String gradeBookName, studentName, assignmentName, assignmentScoreStr;
        int assignmentScore ;
        gradeBookName = jTextField8.getText();
        studentName = jTextField1.getText();
        assignmentName = jTextField2.getText();
        
        if(jRadioButton3.isSelected()){
            group.clearSelection();
            if(jTextField8.getText().equalsIgnoreCase("") || jTextField1.getText().equalsIgnoreCase("") || 
            jTextField2.getText().equalsIgnoreCase("") || jTextField3.getText().equalsIgnoreCase("")  ) {
                jTextField6.setText("TODO : Insert appropriate error code");
                jTextArea1.setText("Error : Form incomplete");
              
            }
            else {     
            assignmentScore =  Integer.parseInt(jTextField3.getText()) ;
            Assignment2Client.clientPost(gradeBookName, studentName, assignmentName, assignmentScore);
            
            jTextField4.setText(Assignment2Client.resourceUri);
            jTextField6.setText(Integer.toString(Assignment2Client.serverResponse.getStatus()));
            jTextArea1.setText(Assignment2Client.resourceRepresentation);
            }
        }
        else if (jRadioButton2.isSelected()){
            group.clearSelection();
            if(jTextField8.getText().equalsIgnoreCase("") || jTextField1.getText().equalsIgnoreCase("") || 
            jTextField2.getText().equalsIgnoreCase("") ) {
                jTextField6.setText("");
                jTextArea1.setText("Error : Form incomplete");
              
            }
            else{
                Assignment2Client.clientGet(gradeBookName, studentName, assignmentName);
                jTextField6.setText(Integer.toString(Assignment2Client.serverResponse.getStatus()));
                jTextArea1.setText(Assignment2Client.resourceRepresentation);
                jTextField4.setText(Assignment2Client.resourceUri);
            }
        }
        else if(jRadioButton5.isSelected()){
            group.clearSelection();          
            if(jTextField8.getText().equalsIgnoreCase("") || jTextField1.getText().equalsIgnoreCase("") || 
            jTextField2.getText().equalsIgnoreCase("") || jTextField3.getText().equalsIgnoreCase("")  ) {
                jTextField6.setText("");
                jTextArea1.setText("Error : Form incomplete");
              
            }
            else {     
            assignmentScore =  Integer.parseInt(jTextField3.getText()) ;
            Assignment2Client.clientUpdate(gradeBookName, studentName, assignmentName, assignmentScore);
            jTextField6.setText(Integer.toString(Assignment2Client.serverResponse.getStatus()));
            jTextArea1.setText(Assignment2Client.resourceRepresentation);
            jTextField4.setText(Assignment2Client.resourceUri);
               }  
        }
        else if(jRadioButton4.isSelected()){
            group.clearSelection();
            if(jTextField8.getText().equalsIgnoreCase("") || jTextField1.getText().equalsIgnoreCase("") || 
            jTextField2.getText().equalsIgnoreCase("")  ) {
                jTextField6.setText("");
                jTextArea1.setText("Error : Form incomplete");             
            }
            else {     
            Assignment2Client.clientDelete(gradeBookName, studentName, assignmentName);
            jTextField6.setText(Integer.toString(Assignment2Client.serverResponse.getStatus()));
            jTextArea1.setText(Assignment2Client.resourceRepresentation);
            jTextField4.setText(Assignment2Client.resourceUri);
            }
        }
        else {
            jTextArea1.setText("Error : Please select an operation to perform");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

